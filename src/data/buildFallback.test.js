import { describe, it, expect } from 'vitest';
import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { buildFromYearJson } from './buildFallback.js';

// Regression test for the standings bug where computeStandings ignored the
// authoritative per-driver points the bundle ships in `results[r].detail`
// and `results[r].sprintResults.detail`, and instead approximated both
// (race FL +1 bonus that the FIA dropped in 2025; sprint points derived
// from main race finishing order rather than the actual sprint result).
//
// The 2026 bundle is the live current season - the nightly refresh grows it
// race by race, so hardcoded totals would drift and rot. Instead of pinning
// magic numbers, we independently re-derive the canonical totals straight from
// the same `detail` maps computeStandings is supposed to read, and assert the
// two agree. That never drifts, and it's a STRONGER regression test: any
// reversion to an approximation (the old +1 FL bonus, or sprint points from
// race order) makes at least one canonical total diverge.

const bundle = JSON.parse(
  readFileSync(resolve(process.cwd(), 'public/data/2026.json'), 'utf8')
);

// Sum the bundle's own canonical per-round detail points: race points from
// results[r].detail[code].points, sprint points from
// results[r].sprintResults.detail[code].points. Constructor points are
// attributed per round via each row's `team`, so a mid-season team switch is
// still counted correctly.
function canonicalTotals(b) {
  const driverPts = {}, driverWins = {}, teamPts = {};
  for (const round of Object.keys(b.results || {})) {
    const r = b.results[round];
    const teamByCode = {};
    for (const [code, d] of Object.entries(r.detail || {})) {
      driverPts[code] = (driverPts[code] || 0) + (d.points || 0);
      if (String(d.position) === '1') driverWins[code] = (driverWins[code] || 0) + 1;
      if (d.team) { teamByCode[code] = d.team; teamPts[d.team] = (teamPts[d.team] || 0) + (d.points || 0); }
    }
    for (const [code, d] of Object.entries(r.sprintResults?.detail || {})) {
      driverPts[code] = (driverPts[code] || 0) + (d.points || 0);
      const t = d.team || teamByCode[code];
      if (t) teamPts[t] = (teamPts[t] || 0) + (d.points || 0);
    }
  }
  return { driverPts, driverWins, teamPts };
}

describe('buildFromYearJson computeStandings (2026 bundle)', () => {
  const D = buildFromYearJson(bundle);
  const { drivers, teams } = D.computeStandings();
  const canon = canonicalTotals(bundle);

  it('has a populated table (the live bundle actually loaded)', () => {
    expect(drivers.length).toBeGreaterThan(0);
    expect(teams.length).toBeGreaterThan(0);
  });

  it('driver points equal the canonical detail totals (race + sprint, no FL-bonus / sprint approximation)', () => {
    for (const row of drivers) {
      expect(row.points).toBe(canon.driverPts[row.driver.id] ?? 0);
    }
  });

  it('driver wins equal the canonical count of P1 finishes', () => {
    for (const row of drivers) {
      expect(row.wins).toBe(canon.driverWins[row.driver.id] ?? 0);
    }
  });

  it('constructor points equal the canonical per-team totals (Top-3 panel and standings page agree)', () => {
    for (const row of teams) {
      expect(row.points).toBe(canon.teamPts[row.team.id] ?? 0);
    }
  });

  it('ranks drivers by points, highest first', () => {
    for (let i = 1; i < drivers.length; i++) {
      expect(drivers[i - 1].points).toBeGreaterThanOrEqual(drivers[i].points);
    }
  });

  it('ranks constructors by points, highest first', () => {
    for (let i = 1; i < teams.length; i++) {
      expect(teams[i - 1].points).toBeGreaterThanOrEqual(teams[i].points);
    }
  });
});

// Historic seasons (1950-2019) live in gitignored bundles regenerated by
// `npm run build:archive`. Skip when they're not on disk (fresh clone
// running tests before prebuild) so the suite isn't flaky in that
// environment. The CI deploy always builds before any verification.
const HISTORIC = [
  { year: 1950, champion: 'farina',               points: 30   }, // best 4 of 7
  { year: 1958, champion: 'hawthorn',             points: 42   }, // best 6 of 11
  { year: 1961, champion: 'phil_hill',            points: 34   }, // best 5 of 8
  { year: 1964, champion: 'surtees',              points: 40   }, // drop-rule inversion: gross winner was Hill 41 / Surtees 40
  { year: 1976, champion: 'hunt',                 points: 69   },
  { year: 1990, champion: 'senna',                points: 78   },
  { year: 2000, champion: 'michael_schumacher',   points: 108  },
  { year: 2008, champion: 'hamilton',             points: 98   },
  { year: 2015, champion: 'hamilton',             points: 381  },
  { year: 2019, champion: 'hamilton',             points: 413  },
];

const historicAvailable = HISTORIC.every(e => {
  try {
    JSON.parse(readFileSync(resolve(process.cwd(), `public/data/${e.year}.json`), 'utf8'));
    return true;
  } catch { return false; }
});

const historicDescribe = historicAvailable ? describe : describe.skip;
historicDescribe('buildFromYearJson computeStandings (historic seasons via CSV importer)', () => {
  // Each year covers a different points system - 1976 (9-6-4-3-2-1),
  // 1990 (9-6-4-3-2-1 + drop), 2000 (10-6-4-3-2-1), 2008
  // (10-8-6-5-4-3-2-1), 2015/2019 (modern 25-18-...). Champion totals
  // taken from the official F1 record book. If these drift after a CSV
  // refresh, build-archive.mjs has likely stopped emitting
  // detail[code].points correctly.
  for (const e of HISTORIC) {
    it(`${e.year}: ${e.champion} = ${e.points} pts`, () => {
      const raw = JSON.parse(readFileSync(resolve(process.cwd(), `public/data/${e.year}.json`), 'utf8'));
      const { drivers } = buildFromYearJson(raw).computeStandings();
      expect(drivers[0].driver.id).toBe(e.champion);
      expect(drivers[0].points).toBe(e.points);
    });
  }
});

describe('buildFromYearJson computeStandings (legacy fallback)', () => {
  // Older bundles (pre-detail) only carry `order`, `fastest`,
  // `sprintWinner`. Make sure those still produce a non-zero
  // championship table via the legacy 25-18-15... approximation, so we
  // don't regress historic seasons.
  const legacy = {
    seasonYear: '1999',
    teams: [
      { id: 't1', name: 'Alpha', short: 'AL', color: '#000' },
      { id: 't2', name: 'Beta',  short: 'BE', color: '#111' },
    ],
    drivers: [
      { id: 'AA', first: 'Ann',  last: 'Apple',  team: 't1', num: 1 },
      { id: 'BB', first: 'Bob',  last: 'Berry',  team: 't1', num: 2 },
      { id: 'CC', first: 'Cal',  last: 'Cherry', team: 't2', num: 3 },
      { id: 'DD', first: 'Dee',  last: 'Date',   team: 't2', num: 4 },
    ],
    calendar: [{ round: 1, name: 'Race', circuit: 'x', date: '1999-01-01' }],
    results: {
      1: {
        order: ['AA', 'BB', 'CC', 'DD'],
        fastest: 'AA',
        pole: 'AA',
        dnfs: [],
      },
    },
  };
  const { drivers } = buildFromYearJson(legacy).computeStandings();
  const byCode = Object.fromEntries(drivers.map(r => [r.driver.id, r]));

  it('uses 25-18-15-12 + 1 FL when bundle has no detail map', () => {
    expect(byCode.AA.points).toBe(25 + 1); // P1 + FL
    expect(byCode.BB.points).toBe(18);
    expect(byCode.CC.points).toBe(15);
    expect(byCode.DD.points).toBe(12);
  });
});

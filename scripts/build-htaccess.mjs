// Emit public/.htaccess with Apache mod_rewrite rules that turn the
// legacy ?id= / ?round= query-string URLs into proper 301 redirects to
// the prerendered Astro routes.
//
// Why bother: the existing /driver.html?id=NOR style URLs each ship a
// tiny client-side redirector. Search engines DO follow JS redirects but
// pass less link equity than a server 301 - any backlinks pointing at the
// legacy URLs are diluted. A 301 at the server layer fully consolidates
// equity onto the canonical /drivers/<ref>/ page.
//
// Apache assumption: most FTP-deployed shared hosting is Apache, where
// .htaccess just works. If the host turns out to be nginx / IIS /
// Cloudflare Pages, this file is silently ignored and the existing JS
// fallback in the redirect HTMLs still runs. So generating it is a
// strict improvement with no downside.
//
// Output is gitignored (rebuilt on every prebuild from the same archive
// indexes that drive /driver.html etc.).

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, join } from 'node:path';

const ARCHIVE = resolve(process.cwd(), 'public/data/archive');
const OUT = resolve(process.cwd(), 'public/.htaccess');

function readJsonSafe(path) {
  if (!existsSync(path)) return null;
  return JSON.parse(readFileSync(path, 'utf8'));
}

const codes = readJsonSafe(join(ARCHIVE, '_driver-codes.json')) || {};

const lines = [];
lines.push('# Generated by scripts/build-htaccess.mjs - do not edit by hand.');
lines.push('# Apache mod_rewrite rules: legacy ?id=... query URLs → /drivers/, /races/, /circuits/, /teams/ 301s.');
lines.push('# Ignored on non-Apache hosts; the JS fallback in public/*.html covers those.');
lines.push('');
lines.push('<IfModule mod_rewrite.c>');
lines.push('  RewriteEngine On');
lines.push('');

// ---- driver.html?id=CODE → /drivers/<ref>/ ----
lines.push('  # /driver.html?id=<code> → /drivers/<ref>/');
const sortedCodes = Object.keys(codes).sort();
for (const code of sortedCodes) {
  const ref = codes[code];
  // Match exact code, case-insensitive (NC) so /driver.html?id=nor works too.
  lines.push(`  RewriteCond %{QUERY_STRING} ^id=${code}$ [NC]`);
  lines.push(`  RewriteRule ^driver\\.html$ /drivers/${ref}/? [R=301,L]`);
}
// Unknown / missing id → standings page (mirrors driver.html's JS fallback).
lines.push('  RewriteRule ^driver\\.html$ /standings-drivers/? [R=301,L]');
lines.push('');

// ---- circuit.html?id=X → /circuits/X/ ----
// Generic rule: circuitRef == id. If the ref is unknown, the destination
// 404s - matches the JS fallback's behaviour of redirecting to the index.
lines.push('  # /circuit.html?id=<ref> → /circuits/<ref>/');
lines.push('  RewriteCond %{QUERY_STRING} ^id=([a-zA-Z0-9_-]+)$');
lines.push('  RewriteRule ^circuit\\.html$ /circuits/%1/? [R=301,L]');
lines.push('  RewriteRule ^circuit\\.html$ /circuits/? [R=301,L]');
lines.push('');

// ---- team.html?id=X → /teams/X/ ----
lines.push('  # /team.html?id=<ref> → /teams/<ref>/');
lines.push('  RewriteCond %{QUERY_STRING} ^id=([a-zA-Z0-9_-]+)$');
lines.push('  RewriteRule ^team\\.html$ /teams/%1/? [R=301,L]');
lines.push('  RewriteRule ^team\\.html$ /standings-constructors/? [R=301,L]');
lines.push('');

// ---- race.html?round=N&year=Y (and reversed) → /races/Y/N/ ----
// In Apache, %N back-references resolve against the LAST RewriteCond only,
// so a two-cond AND-match can't easily capture both round and year. List
// each param ordering explicitly with a single anchored RewriteCond.
lines.push('  # /race.html?round=<n>&year=<y> → /races/<y>/<n>/');
lines.push('  RewriteCond %{QUERY_STRING} ^round=(\\d+)&year=(\\d+)$');
lines.push('  RewriteRule ^race\\.html$ /races/%2/%1/? [R=301,L]');
lines.push('  # /race.html?year=<y>&round=<n> → /races/<y>/<n>/');
lines.push('  RewriteCond %{QUERY_STRING} ^year=(\\d+)&round=(\\d+)$');
lines.push('  RewriteRule ^race\\.html$ /races/%1/%2/? [R=301,L]');
lines.push('  # No year (legacy JS used localStorage; server can\'t replicate) or no params → calendar.');
lines.push('  RewriteRule ^race\\.html$ /calendar/? [R=301,L]');

lines.push('</IfModule>');
lines.push('');

writeFileSync(OUT, lines.join('\n'));
console.log(`[htaccess] wrote ${sortedCodes.length} driver code rules + 7 generic rules → ${OUT}`);

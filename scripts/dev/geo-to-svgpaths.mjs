#!/usr/bin/env node
// One-off (NOT in prebuild): converts a Natural Earth 1:110m admin-0 GeoJSON
// into a compact, self-contained set of SVG path strings keyed by ISO-2 code,
// written to src/data/worldGeo.js. The world-map island renders those paths and
// recolours the F1 nations — no runtime map/geo library ships to the browser.
//
// Regenerate only if the base map changes (it doesn't, really):
//   node scripts/dev/geo-to-svgpaths.mjs data/geo/world-110m.geojson
//
// Equirectangular projection, cropped to exclude Antarctica, coords rounded to
// keep the payload small. Source: Natural Earth via natural-earth-vector.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..', '..');

const src = process.argv[2] || join(ROOT, 'data', 'geo', 'world-110m.geojson');
const geo = JSON.parse(readFileSync(src, 'utf8'));

// Projection window: full longitude, latitude cropped to drop Antarctica and
// the empty far-north. Gives a familiar, non-distracting world rectangle.
const W = 1000;
const LAT_TOP = 84;
const LAT_BOTTOM = -56;
const H = Math.round((W / 360) * (LAT_TOP - LAT_BOTTOM));
const DEC = 1; // decimal places kept per coordinate

const projX = (lon) => ((lon + 180) / 360) * W;
const projY = (lat) => ((LAT_TOP - lat) / (LAT_TOP - LAT_BOTTOM)) * H;
const r = (n) => {
  const f = 10 ** DEC;
  return Math.round(n * f) / f;
};

function ringToPath(ring) {
  let d = '';
  let px = null;
  let py = null;
  for (const [lon, lat] of ring) {
    const x = r(projX(lon));
    const y = r(projY(lat));
    if (x === px && y === py) continue; // drop consecutive duplicates
    d += `${d ? 'L' : 'M'}${x} ${y}`;
    px = x;
    py = y;
  }
  return d ? d + 'Z' : '';
}

function polysToPath(geometry) {
  const parts = [];
  const polys = geometry.type === 'Polygon' ? [geometry.coordinates]
    : geometry.type === 'MultiPolygon' ? geometry.coordinates
    : [];
  for (const poly of polys) {
    for (const ring of poly) {
      if (ring.length < 4) continue; // too small to matter at this scale
      const p = ringToPath(ring);
      // Keep only rings with real extent (skips slivers that collapse on rounding)
      if (p && p.length > 12) parts.push(p);
    }
  }
  return parts.join('');
}

const paths = {};
let kept = 0;
let skipped = 0;
for (const f of geo.features) {
  const p = f.properties || {};
  // Natural Earth stamps France, Norway, Kosovo etc. with ISO_A2 "-99" but
  // carries the real code in ISO_A2_EH — so treat "-99" as empty and fall back.
  const primary = (p.ISO_A2 || '').trim();
  let iso = (primary && primary !== '-99') ? primary : (p.ISO_A2_EH || '').trim();
  if (iso === '-99') iso = '';
  if (!iso) { skipped++; continue; }
  if (iso === 'AQ') { skipped++; continue; } // Antarctica: cropped out
  const d = polysToPath(f.geometry);
  if (!d) { skipped++; continue; }
  // If the code repeats (France + territories etc.), concatenate.
  paths[iso] = (paths[iso] || '') + d;
  kept++;
}

const out = `// AUTO-GENERATED by scripts/dev/geo-to-svgpaths.mjs — do not hand-edit.
// Natural Earth 1:110m admin-0 boundaries, equirectangular, keyed by ISO-2.
// Rendered by the /map/ world-map island; no geo library ships to the browser.
export const WORLD_VIEWBOX = '0 0 ${W} ${H}';
export const WORLD_PATHS = ${JSON.stringify(paths)};
`;

const dest = join(ROOT, 'src', 'data', 'worldGeo.js');
mkdirSync(dirname(dest), { recursive: true });
writeFileSync(dest, out);

const bytes = Buffer.byteLength(out, 'utf8');
console.log(`[geo] ${kept} countries kept, ${skipped} skipped → ${dest}`);
console.log(`[geo] viewBox 0 0 ${W} ${H}, payload ${(bytes / 1024).toFixed(1)} KB`);
